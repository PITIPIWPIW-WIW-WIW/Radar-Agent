from dedup import text_to_vector, cosine_similarity

# Пары "одна и та же статья, разный пересказ" — должны иметь высокое сходство.
# Тексты написаны вручную для теста (не копия реальных статей).
DUPLICATE_PAIRS = [
    (
        "Новый метод RAG использует гибридный поиск, комбинируя векторный "
        "и ключевой поиск для повышения точности retrieval-augmented generation.",
        "Авторы предлагают гибридную схему retrieval-augmented generation: "
        "объединение векторного поиска с поиском по ключевым словам "
        "заметно улучшает точность RAG-систем.",
    ),
    (
        "LoRA-адаптация позволяет дообучать большие языковые модели "
        "на малых датасетах, обновляя лишь небольшую часть параметров.",
        "Метод LoRA даёт возможность fine-tuning LLM на небольших "
        "наборах данных за счёт обучения ограниченного числа весов.",
    ),
    (
        "Модель квантования снижает объём памяти для инференса LLM, "
        "сохраняя приемлемое качество генерации текста.",
        "Квантование весов уменьшает требования к памяти при запуске "
        "языковых моделей, почти не теряя в качестве вывода.",
    ),
]

# Пары "разные статьи на одну общую тему (ML)" — должны иметь заметно
# более низкое сходство, чем пары выше.
DIFFERENT_PAIRS = [
    (
        "Новый метод RAG использует гибридный поиск, комбинируя векторный "
        "и ключевой поиск для повышения точности retrieval-augmented generation.",
        "Исследователи представили новую архитектуру для генерации "
        "изображений на основе диффузионных моделей с ускоренным семплированием.",
    ),
    (
        "LoRA-адаптация позволяет дообучать большие языковые модели "
        "на малых датасетах, обновляя лишь небольшую часть параметров.",
        "В статье описан алгоритм обучения с подкреплением для "
        "робототехники, использующий имитационное обучение по видео.",
    ),
    (
        "Модель квантования снижает объём памяти для инференса LLM, "
        "сохраняя приемлемое качество генерации текста.",
        "Новый бенчмарк оценивает способность языковых моделей "
        "решать задачи по математике на уровне олимпиадных школьников.",
    ),
]


def main():
    print("=" * 60)
    print("ПОХОЖИЕ СТАТЬИ (разный пересказ одной темы) — ожидаем высокое сходство")
    print("=" * 60)
    duplicate_scores = []
    for text_a, text_b in DUPLICATE_PAIRS:
        vec_a = text_to_vector(text_a)
        vec_b = text_to_vector(text_b)
        sim = cosine_similarity(vec_a, vec_b)
        duplicate_scores.append(sim)
        print(f"  сходство = {sim:.4f}")

    print()
    print("=" * 60)
    print("РАЗНЫЕ СТАТЬИ (общая тема ML, но разное содержание) — ожидаем низкое сходство")
    print("=" * 60)
    different_scores = []
    for text_a, text_b in DIFFERENT_PAIRS:
        vec_a = text_to_vector(text_a)
        vec_b = text_to_vector(text_b)
        sim = cosine_similarity(vec_a, vec_b)
        different_scores.append(sim)
        print(f"  сходство = {sim:.4f}")

    min_dup = min(duplicate_scores)
    max_diff = max(different_scores)

    print()
    print("=" * 60)
    print("ИТОГ")
    print("=" * 60)
    print(f"Минимальное сходство среди похожих пар:  {min_dup:.4f}")
    print(f"Максимальное сходство среди разных пар:  {max_diff:.4f}")

    if min_dup > max_diff:
        suggested = round((min_dup + max_diff) / 2, 2)
        print(f"\nЕсть чёткий разрыв между группами. Рекомендуемый порог: {suggested}")
        print(f"(сейчас в .env: DUPLICATE_THRESHOLD={config_threshold()})")
    else:
        print(
            "\nВНИМАНИЕ: группы пересекаются (min похожих <= max разных). "
            "Однозначного порога нет — добавь больше пар и/или "
            "используй более точную модель эмбеддингов."
        )


def config_threshold():
    import config
    return config.DUPLICATE_THRESHOLD


if __name__ == "__main__":
    main()